<?php
// Include config file
require_once "config.php";
 
// Define variables and initialize with empty values
$username = $password = "";
$username_err = $password_err = $login_err = "";
 
// Processing form data when form is submitted
if($_SERVER["REQUEST_METHOD"] == "POST"){
 
    // Check if username is empty
    if(empty(trim($_POST["username"]))){
        $username_err = "Please enter username.";
    } else{
        $username = cleanInput($_POST["username"]);
    }
    
    // Check if password is empty
    if(empty(trim($_POST["password"]))){
        $password_err = "Please enter your password.";
    } else{
        $password = cleanInput($_POST["password"]);
    }
    
    // Validate credentials
    if(empty($username_err) && empty($password_err)){
        // Prepare a select statement
        $sql = "SELECT id, username, password, role FROM users WHERE username = ?";
        
        if($stmt = mysqli_prepare($conn, $sql)){
            // Bind variables to the prepared statement as parameters
            mysqli_stmt_bind_param($stmt, "s", $param_username);
            
            // Set parameters
            $param_username = $username;
            
            // Attempt to execute the prepared statement
            if(mysqli_stmt_execute($stmt)){
                // Store result
                mysqli_stmt_store_result($stmt);
                
                // Check if username exists, if yes then verify password
                if(mysqli_stmt_num_rows($stmt) == 1){                    
                    // Bind result variables
                    mysqli_stmt_bind_result($stmt, $id, $username, $hashed_password, $role);
                    if(mysqli_stmt_fetch($stmt)){
                        if(password_verify($password, $hashed_password)){
                            // Password is correct, so start a new session
                            session_start();
                            
                            // Store data in session variables
                            $_SESSION["loggedin"] = true;
                            $_SESSION["id"] = $id;
                            $_SESSION["username"] = $username;
                            $_SESSION["role"] = $role;                            
                            
                            // Get resident ID if role is 'resident'
                            if($role === "resident") {
                                $sql = "SELECT id, room_id FROM residents WHERE user_id = ?";
                                if($resident_stmt = mysqli_prepare($conn, $sql)) {
                                    mysqli_stmt_bind_param($resident_stmt, "i", $id);
                                    if(mysqli_stmt_execute($resident_stmt)) {
                                        mysqli_stmt_store_result($resident_stmt);
                                        if(mysqli_stmt_num_rows($resident_stmt) == 1) {
                                            mysqli_stmt_bind_result($resident_stmt, $resident_id, $room_id);
                                            mysqli_stmt_fetch($resident_stmt);
                                            $_SESSION["resident_id"] = $resident_id;
                                            $_SESSION["room_id"] = $room_id;
                                        }
                                    }
                                    mysqli_stmt_close($resident_stmt);
                                }
                            }
                            
                            // Redirect user to welcome page
                            if($role === "admin") {
                                header("location: admin/dashboard.php");
                            } else {
                                header("location: resident/dashboard.php");
                            }
                        } else{
                            // Password is not valid, display a generic error message
                            $login_err = "Invalid username or password.";
                        }
                    }
                } else{
                    // Username doesn't exist, display a generic error message
                    $login_err = "Invalid username or password.";
                }
            } else{
                echo "Oops! Something went wrong. Please try again later.";
            }

            // Close statement
            mysqli_stmt_close($stmt);
        }
    }
    
    // Close connection
    mysqli_close($conn);
}
